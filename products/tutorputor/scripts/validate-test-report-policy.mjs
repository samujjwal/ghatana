#!/usr/bin/env node
import { execFileSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";
import process from "node:process";

const repoRoot = process.cwd();
const productRoot = path.join(repoRoot, "products", "tutorputor");
const workflowPath = path.join(productRoot, ".gitea", "workflows", "tutorputor-ci.yml");
const gitignorePath = path.join(repoRoot, ".gitignore");

const forbiddenTrackedArtifacts = [
  /(^|\/)test_results\.txt$/,
  /(^|\/)test-results\.json$/,
  /(^|\/)test-results\.xml$/,
  /(^|\/)playwright-report\//,
];

const trackedFiles = execFileSync("git", ["ls-files", "products/tutorputor"], {
  cwd: repoRoot,
  encoding: "utf8",
})
  .split(/\r?\n/)
  .filter(Boolean)
  .map((file) => file.replace(/\\/g, "/"));

const errors = [];

for (const file of trackedFiles) {
  if (
    fs.existsSync(path.join(repoRoot, file)) &&
    forbiddenTrackedArtifacts.some((pattern) => pattern.test(file))
  ) {
    errors.push(`Tracked stale test evidence must be generated by CI instead: ${file}`);
  }
}

const workflow = fs.existsSync(workflowPath) ? fs.readFileSync(workflowPath, "utf8") : "";
const requiredWorkflowMarkers = [
  "Validate test report artifact policy",
  "unit-test-report",
  "integration-test-report",
  "e2e-test-report",
  "accessibility-test-report",
  "performance-test-report",
  "resilience-test-report",
  "coverage-report",
  "if-no-files-found: error",
];

for (const marker of requiredWorkflowMarkers) {
  if (!workflow.includes(marker)) {
    errors.push(`CI workflow does not publish required fresh report artifact marker: ${marker}`);
  }
}

const gitignore = fs.existsSync(gitignorePath) ? fs.readFileSync(gitignorePath, "utf8") : "";
const requiredIgnoreMarkers = [
  "**/playwright-report/",
  "**/test-results.json",
  "**/test-results.xml",
  "**/test_results.txt",
];

for (const marker of requiredIgnoreMarkers) {
  if (!gitignore.includes(marker)) {
    errors.push(`.gitignore does not keep generated test evidence out of source: ${marker}`);
  }
}

if (errors.length > 0) {
  console.error("Test report artifact policy validation failed:");
  for (const error of errors) {
    console.error(`- ${error}`);
  }
  process.exit(1);
}

console.log("Test reports are generated as CI artifacts; stale committed result evidence is blocked.");

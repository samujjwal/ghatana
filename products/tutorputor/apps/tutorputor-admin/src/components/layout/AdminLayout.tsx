export function AdminLayout() {
  const { user, logout } = useAuth();
  const location = useLocation();
  const [showCommandPalette, setShowCommandPalette] = useState(false);
  const [isMobileMenuOpen, setIsMobileMenuOpen] = useState(false);
  const navListRef = useRef<HTMLUListElement>(null);
  const quickCreateRef = useRef<HTMLDivElement>(null);

  // Enable keyboard navigation for main navigation (arrow keys)
  useKeyboardNavigation({
    containerRef: navListRef,
    itemSelector: 'a[role="link"]',
    orientation: "vertical",
    loop: true,
  });

  // Enable keyboard navigation for Quick Create section
  useKeyboardNavigation({
    containerRef: quickCreateRef,
    itemSelector: 'a[role="listitem"]',
    orientation: "vertical",
    loop: true,
  });

  // Command palette keyboard shortcut (Cmd+K / Ctrl+K)
  useState(() => {
    const handler = (e: KeyboardEvent) => {
      if ((e.metaKey || e.ctrlKey) && e.key === "k") {
        e.preventDefault();
        setShowCommandPalette(true);
      }
      if (e.key === "Escape") {
        setShowCommandPalette(false);
      }
    };
    window.addEventListener("keydown", handler);
    return () => window.removeEventListener("keydown", handler);
  });

  return (
    <div className="min-h-screen bg-gray-50 dark:bg-gray-900">
      {/* Skip to main content link for accessibility */}
      <a
        href="#main-content"
        className="sr-only focus:not-sr-only focus:absolute focus:top-2 focus:left-2 focus:z-50 focus:px-4 focus:py-2 focus:bg-primary-600 focus:text-white focus:rounded-lg focus:shadow-lg"
      >
        Skip to main content
      </a>

      {/* Mobile Header */}
      <div className="lg:hidden fixed top-0 left-0 right-0 h-14 bg-white dark:bg-gray-800 border-b border-gray-200 dark:border-gray-700 flex items-center justify-between px-4 z-50">
        <button
          onClick={() => setIsMobileMenuOpen(!isMobileMenuOpen)}
          className="p-2 rounded-lg hover:bg-gray-100 dark:hover:bg-gray-700 focus:outline-none focus:ring-2 focus:ring-primary-500 focus:ring-offset-2"
          aria-label={isMobileMenuOpen ? "Close menu" : "Open menu"}
          aria-expanded={isMobileMenuOpen}
          aria-controls="mobile-navigation"
        >
          <MenuIcon className="w-6 h-6" />
        </button>
        <h1 className="text-lg font-bold text-primary-600">TutorPutor</h1>
        <div className="w-10" /> {/* Spacer for centering */}
      </div>

      {/* Mobile Menu Overlay */}
      {isMobileMenuOpen && (
        <div
          className="lg:hidden fixed inset-0 bg-black bg-opacity-50 z-40"
          onClick={() => setIsMobileMenuOpen(false)}
        />
      )}

      {/* Command Palette Overlay */}
      {showCommandPalette && (
        <CommandPalette
          onClose={() => setShowCommandPalette(false)}
          actions={quickActions}
        />
      )}

      {/* Sidebar - Compact, responsive */}
      <aside
        id="mobile-navigation"
        className={`fixed inset-y-0 left-0 w-56 bg-white dark:bg-gray-800 border-r border-gray-200 dark:border-gray-700 flex flex-col z-40 transition-transform duration-300 lg:translate-x-0 ${isMobileMenuOpen ? "translate-x-0" : "-translate-x-full"} lg:block`}
        aria-label="Main navigation"
        role="navigation"
      >
        {/* Logo */}
        <div className="h-14 flex items-center px-4 border-b border-gray-200 dark:border-gray-700">
          <h1 className="text-lg font-bold text-primary-600">TutorPutor</h1>
          <span className="ml-2 text-xs text-gray-400 bg-gray-100 dark:bg-gray-700 px-1.5 py-0.5 rounded">
            Admin
          </span>
        </div>

        {/* Quick Search */}
        <div className="px-3 py-2 border-b border-gray-100 dark:border-gray-700">
          <button
            onClick={() => setShowCommandPalette(true)}
            className="w-full flex items-center gap-2 px-3 py-2 text-sm text-gray-500 bg-gray-50 dark:bg-gray-700/50 hover:bg-gray-100 dark:hover:bg-gray-700 rounded-lg transition-colors focus:outline-none focus:ring-2 focus:ring-primary-500"
            aria-label="Open command palette (⌘K)"
          >
            <SearchIcon className="w-4 h-4" />
            <span className="flex-1 text-left">Quick actions...</span>
            <kbd className="text-xs bg-gray-200 dark:bg-gray-600 px-1.5 py-0.5 rounded">
              ⌘K
            </kbd>
          </button>
        </div>

        {/* Navigation */}
        <nav
          className="flex-1 px-2 py-4 overflow-y-auto"
          aria-label="Primary navigation"
        >
          <ul ref={navListRef} className="space-y-1" role="list">
            {navigation.map((item) => {
              const isActive = location.pathname.startsWith(item.href);
              return (
                <li key={item.name}>
                  <NavLink
                    to={item.href}
                    onClick={() => setIsMobileMenuOpen(false)}
                    role="link"
                    className={`flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-all ${
                      isActive
                        ? "bg-primary-50 dark:bg-primary-900/20 text-primary-600 dark:text-primary-400 shadow-sm"
                        : "text-gray-700 dark:text-gray-300 hover:bg-gray-100 dark:hover:bg-gray-700"
                    }`}
                  >
                    <item.icon
                      className={`w-5 h-5 ${isActive ? "text-primary-500" : ""}`}
                    />
                    <div className="flex-1">
                      <div>{item.name}</div>
                      <div className="text-xs text-gray-400 font-normal">
                        {item.description}
                      </div>
                    </div>
                  </NavLink>
                </li>
              );
            })}
          </ul>

          {/* Quick Create Section */}
          <div
            className="mt-6 px-2"
            role="region"
            aria-label="Quick create actions"
          >
            <h3
              className="text-xs font-semibold text-gray-400 dark:text-gray-500 uppercase tracking-wide mb-2"
              id="quick-create-heading"
            >
              Quick Create
            </h3>
            <div
              ref={quickCreateRef}
              className="space-y-1"
              role="list"
              aria-labelledby="quick-create-heading"
            >
              <NavLink
                to="/authoring/new?tab=simulation"
                onClick={() => setIsMobileMenuOpen(false)}
                className="flex items-center gap-2 px-3 py-2 text-sm text-gray-600 dark:text-gray-400 hover:bg-gray-100 dark:hover:bg-gray-700 rounded-lg transition-colors focus:outline-none focus:ring-2 focus:ring-primary-500 focus:ring-inset"
                role="listitem"
              >
                <BeakerIcon className="w-4 h-4" />
                <span>Simulation</span>
              </NavLink>
              <NavLink
                to="/authoring/new?tab=animation"
                onClick={() => setIsMobileMenuOpen(false)}
                className="flex items-center gap-2 px-3 py-2 text-sm text-gray-600 dark:text-gray-400 hover:bg-gray-100 dark:hover:bg-gray-700 rounded-lg transition-colors focus:outline-none focus:ring-2 focus:ring-primary-500 focus:ring-inset"
                role="listitem"
                aria-label="Create new animation"
              >
                <FilmIcon className="w-4 h-4" />
                <span>Animation</span>
              </NavLink>
              <NavLink
                to="/examples"
                onClick={() => setIsMobileMenuOpen(false)}
                className="flex items-center gap-2 px-3 py-2 text-sm text-gray-600 dark:text-gray-400 hover:bg-gray-100 dark:hover:bg-gray-700 rounded-lg transition-colors focus:outline-none focus:ring-2 focus:ring-primary-500 focus:ring-inset"
                role="listitem"
                aria-label="View examples gallery"
              >
                <BookOpenIcon className="w-4 h-4" />
                <span>Examples</span>
              </NavLink>
            </div>
          </div>
        </nav>

        {/* AI Status Indicator */}
        <div className="px-3 py-2 border-t border-gray-100 dark:border-gray-700">
          <div className="flex items-center gap-2 px-3 py-2 bg-gradient-to-r from-purple-50 to-blue-50 dark:from-purple-900/20 dark:to-blue-900/20 rounded-lg">
            <div className="w-2 h-2 bg-green-500 rounded-full animate-pulse" />
            <span className="text-xs text-gray-600 dark:text-gray-300">
              AI Ready
            </span>
            <SparklesIcon className="w-4 h-4 text-purple-500 ml-auto" />
          </div>
        </div>

        {/* User Menu */}
        <div className="p-3 border-t border-gray-200 dark:border-gray-700">
          <div className="flex items-center gap-2">
            <div className="w-8 h-8 rounded-full bg-primary-100 dark:bg-primary-900 flex items-center justify-center">
              <span className="text-primary-600 dark:text-primary-400 text-sm font-medium">
                {user?.displayName?.charAt(0).toUpperCase() || "A"}
              </span>
            </div>
            <div className="flex-1 min-w-0">
              <p className="text-sm font-medium text-gray-900 dark:text-white truncate">
                {user?.displayName || "Admin"}
              </p>
            </div>
            <button
              onClick={logout}
              className="p-1.5 text-gray-400 hover:text-gray-600 dark:hover:text-gray-300 rounded hover:bg-gray-100 dark:hover:bg-gray-700"
              title="Sign out"
            >
              <LogoutIcon className="w-4 h-4" />
            </button>
          </div>
        </div>
      </aside>

      {/* Main Content */}
      {/* Main content area - responsive */}
      <main
        id="main-content"
        className="lg:pl-56 pt-14 lg:pt-0"
        role="main"
        aria-label="Main content"
      >
        <Outlet />
      </main>
    </div>
  );
}

// Command Palette Component
function CommandPalette({
  onClose,
  actions,
}: {
  onClose: () => void;
  actions: typeof quickActions;
}) {
  return (
    <div className="fixed inset-0 z-50 flex items-start justify-center pt-[20vh]">
      <div className="absolute inset-0 bg-black/50" onClick={onClose} />
      <div className="relative w-full max-w-lg bg-white dark:bg-gray-800 rounded-xl shadow-2xl border border-gray-200 dark:border-gray-700 overflow-hidden">
        <div className="flex items-center gap-3 px-4 py-3 border-b border-gray-200 dark:border-gray-700">
          <SearchIcon className="w-5 h-5 text-gray-400" />
          <input
            type="text"
            placeholder="Type a command or search..."
            className="flex-1 bg-transparent border-none outline-none text-gray-900 dark:text-white placeholder-gray-400"
            autoFocus
          />
          <kbd className="text-xs bg-gray-100 dark:bg-gray-700 px-2 py-1 rounded">
            ESC
          </kbd>
        </div>
        <div className="max-h-80 overflow-y-auto p-2">
          {actions.map((action) => (
            <a
              key={action.id}
              href={action.action}
              className="flex items-center gap-3 px-3 py-2 rounded-lg hover:bg-gray-100 dark:hover:bg-gray-700 transition-colors"
              onClick={onClose}
            >
              <span className="flex-1 text-sm text-gray-900 dark:text-white">
                {action.label}
              </span>
              <kbd className="text-xs bg-gray-100 dark:bg-gray-600 px-1.5 py-0.5 rounded">
                {action.shortcut}
              </kbd>
            </a>
          ))}
        </div>
      </div>
    </div>
  );
}


function useAnnouncer() {
  return (message: string, delayMs: number = 0) => {
    if (typeof window === "undefined") {
      return;
    }

    window.setTimeout(() => {
      const liveRegion = document.getElementById("authoring-live-region");
      if (liveRegion) {
        liveRegion.textContent = message;
      }
    }, delayMs);
  };
}


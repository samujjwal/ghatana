// Simulation Editor Component - Full Featured Physics Builder
function SimulationEditor({
  contentId,
  domain,
}: {
  contentId: string;
  domain: string;
}) {
  const [claims, setClaims] = useState<
    Array<{ id: string; text: string; claimRef?: string }>
  >([]);
  const [selectedClaimRef, setSelectedClaimRef] = useState("");
  const [manifestId, setManifestId] = useState<string | null>(null);
  const [initialManifest, setInitialManifest] = useState<SimulationManifest | undefined>(
    undefined,
  );
  const [isLoading, setIsLoading] = useState(true);

  useEffect(() => {
    let cancelled = false;

    const loadSimulationContext = async () => {
      setIsLoading(true);
      try {
        const [experienceResponse, simulationsResponse] = await Promise.all([
          fetch(`/api/content-studio/experiences/${contentId}/comprehensive`, {
            headers: getAuthoringApiHeaders(false),
          }),
          fetch(`/api/content-studio/experiences/${contentId}/simulations`, {
            headers: getAuthoringApiHeaders(false),
          }),
        ]);

        if (!experienceResponse.ok) {
          throw new Error(`Failed to load experience (${experienceResponse.status})`);
        }
        if (!simulationsResponse.ok) {
          throw new Error(`Failed to load simulations (${simulationsResponse.status})`);
        }

        const experience =
          (await experienceResponse.json()) as ComprehensiveExperienceResponse;
        const simulations = (await simulationsResponse.json()) as {
          data: PersistedSimulationLink[];
        };

        if (cancelled) {
          return;
        }

        const mappedClaims = (experience.data.claims ?? []).map((claim) => ({
          id: claim.id,
          text: claim.text ?? claim.claimRef ?? "Untitled claim",
          claimRef: claim.claimRef ?? claim.id,
        }));
        const linkedSimulation = simulations.data?.[0];

        setClaims(mappedClaims);
        setSelectedClaimRef(
          linkedSimulation?.claimRef ?? mappedClaims[0]?.claimRef ?? "",
        );
        setManifestId(linkedSimulation?.simulationManifestId ?? null);
        setInitialManifest(
          (linkedSimulation?.simulationManifest?.manifest as SimulationManifest | undefined) ??
          undefined,
        );
      } catch (_error) {
        toast.error("Failed to load simulation context");
      } finally {
        if (!cancelled) {
          setIsLoading(false);
        }
      }
    };

    void loadSimulationContext();

    return () => {
      cancelled = true;
    };
  }, [contentId]);

  const normalizedDomain = ((domain || "PHYSICS").toUpperCase() === "MATH"
    ? "MATHEMATICS"
    : (domain || "PHYSICS").toUpperCase()) as SimulationDomain;

  const saveManifest = useCallback(
    async (manifest: SimulationManifest) => {
      if (!selectedClaimRef) {
        toast.error("Select a claim before saving the simulation.");
        return;
      }

      const payload = {
        ...manifest,
        domain: normalizedDomain,
        title: manifest.title || `Simulation for ${contentId}`,
        description: manifest.description || "",
        version: manifest.version || "1.0.0",
      };

      try {
        let currentManifestId = manifestId;

        if (!currentManifestId) {
          const createResponse = await fetch("/api/sim-author/manifests", {
            method: "POST",
            headers: getAuthoringApiHeaders(),
            body: JSON.stringify(payload),
          });

          if (!createResponse.ok) {
            throw new Error(`Create failed (${createResponse.status})`);
          }

          const created = (await createResponse.json()) as {
            id: string;
            manifest?: SimulationManifest;
          };
          currentManifestId = created.id;
          setManifestId(created.id);
        } else {
          const updateResponse = await fetch(
            `/api/sim-author/manifests/${currentManifestId}`,
            {
              method: "PUT",
              headers: getAuthoringApiHeaders(),
              body: JSON.stringify(payload),
            },
          );

          if (!updateResponse.ok) {
            throw new Error(`Save failed (${updateResponse.status})`);
          }
        }

        const linkResponse = await fetch(
          `/api/sim-author/manifests/${currentManifestId}/link-claim`,
          {
            method: "POST",
            headers: getAuthoringApiHeaders(),
            body: JSON.stringify({
              experienceId: contentId,
              claimRef: selectedClaimRef,
              interactionType: "parameter_exploration",
              goal: `Explore ${domain || "the concept"}`,
              estimatedMinutes: 10,
            }),
          },
        );

        if (!linkResponse.ok) {
          throw new Error(`Link failed (${linkResponse.status})`);
        }

        setInitialManifest(payload as SimulationManifest);
        toast.success("Simulation saved successfully");
      } catch (error) {
        console.error("Simulation save error:", error);
        toast.error("Failed to save simulation. Please try again.");
      }
    },
    [contentId, domain, manifestId, normalizedDomain, selectedClaimRef],
  );

  const handleAIGenerate = useCallback(
    async (prompt: string, aiDomain: SimulationDomain) => {
      const response = await fetch("/api/sim-author/generate", {
        method: "POST",
        headers: getAuthoringApiHeaders(),
        body: JSON.stringify({
          prompt,
          domain: aiDomain,
        }),
      });

      if (!response.ok) {
        throw new Error(`Generation failed (${response.status})`);
      }

      const result = (await response.json()) as {
        manifest?: SimulationManifest;
      };

      if (!result.manifest) {
        throw new Error("Simulation generation returned no manifest");
      }

      return result.manifest;
    },
    [],
  );

  const handleNLRefine = useCallback(
    async (input: string): Promise<SimulationRefinementResult> => {
      const manifest = initialManifest;
      if (!manifest) {
        return {
          success: false,
          response: "Generate or load a simulation before refining it.",
          suggestions: [],
        };
      }

      const response = await fetch("/api/sim-author/refine", {
        method: "POST",
        headers: getAuthoringApiHeaders(),
        body: JSON.stringify({
          manifest,
          refinement: input,
        }),
      });

      if (!response.ok) {
        throw new Error(`Refinement failed (${response.status})`);
      }

      const result = (await response.json()) as {
        manifest?: SimulationManifest;
        suggestions?: string[];
      };

      if (result.manifest) {
        setInitialManifest(result.manifest);
      }

      return {
        success: Boolean(result.manifest),
        manifest: result.manifest,
        response: result.manifest
          ? "I refined the simulation manifest based on your instructions."
          : "No manifest changes were returned.",
        suggestions: result.suggestions ?? [],
      };
    },
    [initialManifest],
  );

  return (
    <div className="space-y-4">
      <div className="flex items-center justify-between">
        <h3 className="text-lg font-semibold text-gray-900 dark:text-white">
          Simulation Studio
        </h3>
        <div className="flex items-center gap-2">
          <select
            value={selectedClaimRef}
            onChange={(event) => setSelectedClaimRef(event.target.value)}
            className="rounded-lg border border-gray-200 bg-white px-3 py-2 text-sm text-gray-700 dark:border-gray-600 dark:bg-gray-700 dark:text-white"
          >
            <option value="">Select claim</option>
            {claims.map((claim) => (
              <option key={claim.id} value={claim.claimRef}>
                {claim.text}
              </option>
            ))}
          </select>
          <span className="text-sm text-gray-500">
            Manifest-based simulation authoring
          </span>
        </div>
      </div>

      <div className="h-[760px] overflow-hidden rounded-lg border border-gray-200 bg-white dark:border-gray-700 dark:bg-gray-800">
        {isLoading ? (
          <div className="flex h-full items-center justify-center text-sm text-gray-500">
            Loading simulation context...
          </div>
        ) : (
          <SimulationStudio
            key={manifestId ?? selectedClaimRef ?? "new-simulation"}
            initialManifest={initialManifest}
            onSave={(manifest: Record<string, unknown>) => {
              void saveManifest(manifest as unknown as SimulationManifest);
            }}
            onAIGenerate={handleAIGenerate}
            onNLRefine={handleNLRefine}
          />
        )}
      </div>
    </div>
  );
}

// Animation Editor Component - Full Featured Timeline-Based Animation
function AnimationEditor({ contentId }: { contentId: string }) {
  const [claims, setClaims] = useState<
    Array<{ id: string; text: string; claimRef?: string }>
  >([]);
  const [selectedClaimId, setSelectedClaimId] = useState<string>("");
  const [initialAnimation, setInitialAnimation] = useState<AnimationSpec | undefined>(
    undefined,
  );
  const [isLoading, setIsLoading] = useState(true);

  useEffect(() => {
    let cancelled = false;

    const loadAnimationContext = async () => {
      setIsLoading(true);
      try {
        const [experienceResponse, animationsResponse] = await Promise.all([
          fetch(`/api/content-studio/experiences/${contentId}/comprehensive`, {
            headers: getAuthoringApiHeaders(false),
          }),
          fetch(`/api/content-studio/experiences/${contentId}/animations`, {
            headers: getAuthoringApiHeaders(false),
          }),
        ]);

        if (!experienceResponse.ok) {
          throw new Error(`Failed to load claims (${experienceResponse.status})`);
        }
        if (!animationsResponse.ok) {
          throw new Error(`Failed to load animations (${animationsResponse.status})`);
        }

        const experience =
          (await experienceResponse.json()) as ComprehensiveExperienceResponse;
        const animations = (await animationsResponse.json()) as {
          data: PersistedAnimationLink[];
        };

        if (cancelled) {
          return;
        }

        const mappedClaims = (experience.data.claims ?? []).map((claim) => ({
          id: claim.id,
          text: claim.text ?? claim.claimRef ?? "Untitled claim",
          claimRef: claim.claimRef ?? claim.id,
        }));

        setClaims(mappedClaims);

        const firstClaimRef = mappedClaims[0]?.claimRef ?? "";
        const firstAnimation = animations.data?.[0];
        const claimRefToUse = firstAnimation?.claimId ?? firstClaimRef;

        setSelectedClaimId(claimRefToUse);
        setInitialAnimation(firstAnimation?.spec);
      } catch (_error) {
        toast.error("Failed to load animation context");
      } finally {
        if (!cancelled) {
          setIsLoading(false);
        }
      }
    };

    void loadAnimationContext();

    return () => {
      cancelled = true;
    };
  }, [contentId]);

  const handleSave = async (animation: AnimationSpec) => {
    if (!selectedClaimId) {
      toast.error("Select a claim before saving the animation.");
      return;
    }

    try {
      const selectedClaim = claims.find((claim) => claim.claimRef === selectedClaimId);
      const response = await fetch(`/api/content-studio/experiences/${contentId}/animations`, {
        method: "POST",
        headers: getAuthoringApiHeaders(),
        body: JSON.stringify({
          claimId: selectedClaimId,
          animationId: (animation['id'] as string | undefined) ?? `anim-${contentId}`,
          animationType: "assessment",
          autoGenerated: false,
          concept: selectedClaim?.text ?? (animation['title'] as string | undefined),
          difficulty: "medium",
          estimatedDuration: Math.max(1, Math.round((animation['durationSeconds'] as number | undefined) ?? 30)),
          spec: animation,
        }),
      });
      if (!response.ok) {
        throw new Error(`Save failed: ${response.status}`);
      }
      setInitialAnimation(animation);
      toast.success("Animation saved successfully");
    } catch (err) {
      toast.error("Failed to save animation. Please try again.");
      console.error("Animation save error:", err);
    }
  };

  const handleExport = (animation: AnimationSpec, format: string) => {
    try {
      const blob = new Blob([JSON.stringify(animation, null, 2)], {
        type: "application/json",
      });
      const title = (animation['id'] as string | undefined) ?? contentId;
      const ext = format === "json" ? "json" : format === "gif" ? "gif" : "mp4";
      const filename = `animation-${title}.${ext}`;

      if (format !== "json") {
        // Video/GIF export requires server-side rendering; notify user
        toast.success(
          `${format.toUpperCase()} export has been queued. You will be notified when it is ready.`,
        );
        fetch(`/api/content-studio/content/${contentId}/animation/export`, {
          method: "POST",
          headers: getAuthoringApiHeaders(),
          body: JSON.stringify({ animation, format }),
        }).catch((err) => console.error("Export queue error:", err));
        return;
      }

      // JSON export is immediate (client-side)
      const url = URL.createObjectURL(blob);
      const anchor = document.createElement("a");
      anchor.href = url;
      anchor.download = filename;
      anchor.click();
      URL.revokeObjectURL(url);
      toast.success(`Animation exported as ${filename}`);
    } catch (err) {
      toast.error("Export failed. Please try again.");
      console.error("Animation export error:", err);
    }
  };

  return (
    <div className="space-y-4">
      <div className="flex items-center justify-between">
        <h3 className="text-lg font-semibold text-gray-900 dark:text-white">
          Timeline-Based Animation
        </h3>
        <div className="flex items-center gap-2">
          <select
            value={selectedClaimId}
            onChange={(event) => setSelectedClaimId(event.target.value)}
            className="rounded-lg border border-gray-200 bg-white px-3 py-2 text-sm text-gray-700 dark:border-gray-600 dark:bg-gray-700 dark:text-white"
          >
            <option value="">Select claim</option>
            {claims.map((claim) => (
              <option key={claim.id} value={claim.claimRef}>
                {claim.text}
              </option>
            ))}
          </select>
          <span className="text-sm text-gray-500">
            Create smooth animations with keyframes
          </span>
        </div>
      </div>

      <div className="h-[600px] bg-white dark:bg-gray-800 rounded-lg border border-gray-200 dark:border-gray-700 overflow-hidden">
        {isLoading ? (
          <div className="flex h-full items-center justify-center text-sm text-gray-500">
            Loading animation context...
          </div>
        ) : (
          <AnimationStudio
            key={selectedClaimId || "default-animation"}
            initialAnimation={initialAnimation as unknown as Parameters<typeof AnimationStudio>[0]['initialAnimation']}
            onSave={handleSave as unknown as Parameters<typeof AnimationStudio>[0]['onSave']}
            onExport={handleExport as unknown as Parameters<typeof AnimationStudio>[0]['onExport']}
          />
        )}
      </div>

      <div className="flex items-center justify-between p-4 bg-purple-50 dark:bg-purple-900/20 rounded-lg">
        <div className="flex items-center gap-2 text-sm text-purple-700 dark:text-purple-300">
          <Sparkles className="w-4 h-4" />
          <span>
            Tip: Add keyframes at different times to create smooth transitions.
            Use the timeline to preview your animation.
          </span>
        </div>
      </div>
    </div>
  );
}


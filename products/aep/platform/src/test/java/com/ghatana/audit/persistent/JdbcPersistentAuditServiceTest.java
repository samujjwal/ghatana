    private static SimpleDataSource dataSource;
    private JdbcPersistentAuditService service;

    @BeforeAll
    static void initDatabase() throws SQLException {
        dataSource = new SimpleDataSource("jdbc:h2:mem:audit_test;DB_CLOSE_DELAY=-1;MODE=PostgreSQL");

        try (Connection conn = dataSource.getConnection();
             Statement stmt = conn.createStatement()) {
            // H2-compatible schema mirroring V006__create_audit_trail.sql
            stmt.execute("""
                    CREATE TABLE IF NOT EXISTS audit_trail (
                        id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        tenant_id       VARCHAR(255) NOT NULL,
                        event_type      VARCHAR(255) NOT NULL,
                        entity_type     VARCHAR(100),
                        entity_id       VARCHAR(255),
                        action          VARCHAR(50) NOT NULL,
                        actor           VARCHAR(255),
                        actor_type      VARCHAR(50) DEFAULT 'USER',
                        "timestamp"     TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
                        details         CLOB,
                        previous_state  CLOB,
                        new_state       CLOB,
                        correlation_id  VARCHAR(255),
                        source          VARCHAR(255),
                        ip_address      VARCHAR(45),
                        success         BOOLEAN NOT NULL DEFAULT TRUE,
                        error_message   CLOB
                    )
                    """);

            stmt.execute("CREATE INDEX IF NOT EXISTS idx_audit_tenant_time ON audit_trail (tenant_id, \"timestamp\")");
            stmt.execute("CREATE INDEX IF NOT EXISTS idx_audit_entity ON audit_trail (entity_type, entity_id)");
            stmt.execute("CREATE INDEX IF NOT EXISTS idx_audit_actor ON audit_trail (actor)");
        }
    }

    @BeforeEach
    void setUp() throws SQLException {
        service = new JdbcPersistentAuditService(dataSource);
        try (Connection conn = dataSource.getConnection();
             Statement stmt = conn.createStatement()) {
            stmt.execute("DELETE FROM audit_trail");
        }
    }

    @AfterAll
    static void tearDown() throws SQLException {
        try (Connection conn = dataSource.getConnection();
             Statement stmt = conn.createStatement()) {
            stmt.execute("DROP TABLE IF EXISTS audit_trail");
        }
    }


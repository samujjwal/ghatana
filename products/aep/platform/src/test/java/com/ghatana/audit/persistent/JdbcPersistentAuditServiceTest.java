    // ═══════════════════════════════════════════════════════════════════════════

    @Nested
    @DisplayName("Concurrent Writes")
    class ConcurrencyTests {

        @Test
        @DisplayName("50 concurrent writes all succeed")
        void concurrentWrites() throws Exception {
            int threadCount = 50;
            ExecutorService executor = Executors.newFixedThreadPool(8);
            CountDownLatch latch = new CountDownLatch(threadCount);

            List<Future<?>> futures = new ArrayList<>();
            for (int i = 0; i < threadCount; i++) {
                final int idx = i;
                futures.add(executor.submit(() -> {
                    try {
                        service.record(auditEvent("t1", "CONCURRENT_" + idx, "thread-" + idx,
                                "RES", "r-" + idx, true, Map.of("action", "CREATE")));
                    } finally {
                        latch.countDown();
                    }
                }));
            }

            latch.await(10, TimeUnit.SECONDS);
            executor.shutdown();

            // Verify all 50 written
            assertThat(runPromise(() -> service.countByTenantId("t1"))).isEqualTo(50L);

            // Verify no exceptions
            for (Future<?> f : futures) {
                assertThatCode(f::get).doesNotThrowAnyException();
            }
        }
    }


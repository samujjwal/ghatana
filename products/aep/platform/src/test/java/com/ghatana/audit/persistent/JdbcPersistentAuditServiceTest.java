    private AuditEvent auditEvent(String tenantId, String eventType, String principal,
                                   String resourceType, String resourceId, boolean success,
                                   Map<String, Object> details) {
        return AuditEvent.builder()
                .tenantId(tenantId)
                .eventType(eventType)
                .timestamp(Instant.now())
                .principal(principal)
                .resourceType(resourceType)
                .resourceId(resourceId)
                .success(success)
                .details(details)
                .build();
    }

    private AuditEvent auditEventAt(String tenantId, String eventType, Instant timestamp,
                                     Map<String, Object> details) {
        return AuditEvent.builder()
                .tenantId(tenantId)
                .eventType(eventType)
                .timestamp(timestamp)
                .principal("system")
                .success(true)
                .details(details)
                .build();
    }

    private void seedEvents(String tenantId, int count) {
        for (int i = 0; i < count; i++) {
            service.record(auditEvent(tenantId, "TEST_EVENT", "user-" + i,
                    "RESOURCE", "r-" + i, true, Map.of("action", "CREATE", "index", i)));
        }
    }


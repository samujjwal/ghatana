    @Nested
    @DisplayName("Lifecycle Tests")
    class LifecycleTests {

        @Test
        @DisplayName("Should delegate start() to agent")
        void shouldDelegateStart() {
            when(mockAgent.start()).thenReturn(Promise.complete());

            runPromise(() -> {
                adapter.initialize(OperatorConfig.builder().build());
                return adapter.start();
            });

            verify(mockAgent).start();
        }

        @Test
        @DisplayName("Should delegate stop() to agent shutdown")
        void shouldDelegateStop() {
            when(mockAgent.start()).thenReturn(Promise.complete());
            when(mockAgent.shutdown()).thenReturn(Promise.complete());

            runPromise(() -> {
                adapter.initialize(OperatorConfig.builder().build());
                adapter.start();
                return adapter.stop();
            });

            verify(mockAgent).shutdown();
        }

        @Test
        @DisplayName("Should be healthy when agent not failed")
        void shouldBeHealthyWhenAgentNotFailed() {
            when(mockAgent.getState()).thenReturn(
                AgentStateProto.newBuilder()
                    .setState(AgentStateProto.State.AGENT_STATE_IDLE)
                    .build()
            );
            assertThat(adapter.isHealthy()).isTrue();
        }

        @Test
        @DisplayName("Should be healthy when agent is busy")
        void shouldBeHealthyWhenAgentBusy() {
            when(mockAgent.getState()).thenReturn(
                AgentStateProto.newBuilder()
                    .setState(AgentStateProto.State.AGENT_STATE_BUSY)
                    .build()
            );
            assertThat(adapter.isHealthy()).isTrue();
        }

        @Test
        @DisplayName("Should be unhealthy when agent terminated")
        void shouldBeUnhealthyWhenAgentTerminated() {
            when(mockAgent.getState()).thenReturn(
                AgentStateProto.newBuilder()
                    .setState(AgentStateProto.State.AGENT_STATE_TERMINATED)
                    .build()
            );
            assertThat(adapter.isHealthy()).isFalse();
        }

        @Test
        @DisplayName("Should initialize without errors")
        void shouldInitializeWithoutErrors() {
            OperatorConfig config = OperatorConfig.builder().build();
            runPromise(() -> adapter.initialize(config));
            // No exception means success
        }
    }


    @Nested
    @DisplayName("Construction and Metadata Tests")
    class ConstructionTests {

        @Test
        @DisplayName("Should create adapter with auto-generated OperatorId")
        void shouldCreateAdapterWithAutoGeneratedId() {
            assertThat(adapter).isNotNull();
            assertThat(adapter.getId()).isNotNull();
            assertThat(adapter.getId().toString()).contains("virtualorg", "agent", "ceo", "1.0.0");
        }

        @Test
        @DisplayName("Should set correct operator type")
        void shouldSetCorrectOperatorType() {
            assertThat(adapter.getType()).isEqualTo(OperatorType.STREAM);
        }

        @Test
        @DisplayName("Should build operator name from agent role")
        void shouldBuildOperatorNameFromRole() {
            assertThat(adapter.getName()).contains("Virtual-Org", "CEO", "Agent");
        }

        @Test
        @DisplayName("Should include agent capabilities")
        void shouldIncludeAgentCapabilities() {
            ToolProto tool = ToolProto.newBuilder()
                .setName("code_review")
                .setDescription("Review code changes")
                .build();
            when(mockAgent.getTools()).thenReturn(List.of(tool));

            AgentStreamOperatorAdapter adapterWithTools =
                AgentStreamOperatorAdapterFactory.create(mockAgent, meterRegistry);

            List<String> caps = adapterWithTools.getCapabilities();
            assertThat(caps).contains(
                "agent.task_processing",
                "agent.decision_making",
                "agent.role.ceo",
                "tool.code_review"
            );
        }

        @Test
        @DisplayName("Should reject null agent")
        void shouldRejectNullAgent() {
            OperatorId id = OperatorId.of("test", "agent", "test", "1.0.0");
            assertThatThrownBy(() ->
                new AgentStreamOperatorAdapter(id, null, meterRegistry))
                .isInstanceOf(NullPointerException.class)
                .hasMessageContaining("VirtualOrgAgent must not be null");
        }
    }

    // ========================================================================
    // EVENT → TASK MAPPING TESTS

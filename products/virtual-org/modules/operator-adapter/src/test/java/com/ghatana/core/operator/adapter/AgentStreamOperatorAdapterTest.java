    @Nested
    @DisplayName("Event to Task Mapping Tests")
    class EventToTaskMappingTests {

        @Test
        @DisplayName("Should map Event to TaskRequestProto correctly")
        void shouldMapEventToTask() {
            EventId eventId = EventId.create("evt-1", "agent.task.code_review", "1.0", "tenant-1");

            Map<String, Object> payload = new HashMap<>();
            payload.put("description", "Review PR #123");
            payload.put("pr_id", "123");
            payload.put("repository", "yappc/ghatana");

            GEvent event = GEvent.builder()
                .id(eventId)
                .time(EventTime.now())
                .payload(payload)
                .headers(Collections.emptyMap())
                .stats(EventStats.builder().build())
                .relations(EventRelations.builder().build())
                .build();

            TaskResponseProto mockResponse = TaskResponseProto.newBuilder()
                .setTaskId("evt-1")
                .setSuccess(true)
                .setResult("LGTM - approved")
                .build();
            when(mockAgent.processTask(any())).thenReturn(Promise.of(mockResponse));

            OperatorResult result = runPromise(() -> adapter.process(event));

            verify(mockAgent).processTask(argThat(taskRequest ->
                taskRequest.getTaskId().equals("evt-1") &&
                taskRequest.getDescription().contains("Review PR #123") &&
                taskRequest.getParametersMap().get("pr_id").equals("123") &&
                taskRequest.getParametersMap().get("repository").equals("yappc/ghatana")
            ));

            assertThat(result.isSuccess()).isTrue();
        }

        @Test
        @DisplayName("Should preserve correlation ID in task mapping")
        void shouldPreserveCorrelationId() {
            EventId eventId = EventId.create("evt-2", "agent.task", "1.0", "tenant-1");
            Map<String, String> headers = new HashMap<>();
            headers.put("correlationId", "corr-12345");
            headers.put("causationId", "cause-67890");

            GEvent event = GEvent.builder()
                .id(eventId)
                .time(EventTime.now())
                .payload(Collections.emptyMap())
                .headers(headers)
                .stats(EventStats.builder().build())
                .relations(EventRelations.builder().build())
                .build();

            when(mockAgent.processTask(any())).thenReturn(Promise.of(
                TaskResponseProto.newBuilder()
                    .setTaskId("task-1")
                    .setSuccess(true)
                    .setResult("done")
                    .build()
            ));

            runPromise(() -> adapter.process(event));

            verify(mockAgent).processTask(argThat(taskRequest ->
                taskRequest.getParametersMap().get("correlation_id").equals("corr-12345") &&
                taskRequest.getParametersMap().get("causation_id").equals("cause-67890")
            ));
        }

        @Test
        @DisplayName("Should use event type as description fallback")
        void shouldUseEventTypeAsFallback() {
            EventId eventId = EventId.create("evt-3", "agent.task.analyze_logs", "1.0", "tenant-1");
            GEvent event = GEvent.builder()
                .id(eventId)
                .time(EventTime.now())
                .payload(Collections.emptyMap())
                .headers(Collections.emptyMap())
                .stats(EventStats.builder().build())
                .relations(EventRelations.builder().build())
                .build();

            when(mockAgent.processTask(any())).thenReturn(Promise.of(
                TaskResponseProto.newBuilder()
                    .setTaskId("task-1")
                    .setSuccess(true)
                    .setResult("done")
                    .build()
            ));

            runPromise(() -> adapter.process(event));

            verify(mockAgent).processTask(argThat(taskRequest ->
                taskRequest.getDescription().contains("agent.task.analyze_logs")
            ));
        }
    }

    // ========================================================================
    // TASK RESPONSE → OPERATOR RESULT MAPPING TESTS
    // ========================================================================

    @Nested
    @DisplayName("Task Response Mapping Tests")
    class TaskResponseMappingTests {

        @Test
        @DisplayName("Should map successful TaskResponse to OperatorResult with output event")
        void shouldMapSuccessfulTaskResponse() {
            TaskResponseProto response = TaskResponseProto.newBuilder()
                .setTaskId("task-1")
                .setSuccess(true)
                .setResult("Code review completed: LGTM")
                .build();
            when(mockAgent.processTask(any())).thenReturn(Promise.of(response));

            EventId inputEventId = EventId.create("evt-4", "agent.task.code_review", "1.0", "tenant-1");
            GEvent inputEvent = GEvent.builder()
                .id(inputEventId)
                .time(EventTime.now())
                .payload(Map.of("description", "Review code"))
                .headers(Collections.emptyMap())
                .stats(EventStats.builder().build())
                .relations(EventRelations.builder().build())
                .build();

            OperatorResult result = runPromise(() -> adapter.process(inputEvent));

            assertThat(result.isSuccess()).isTrue();
            assertThat(result.getOutputEvents()).hasSize(1);

            Event outputEvent = result.getOutputEvents().get(0);
            assertThat(outputEvent.getType()).isEqualTo("agent.task.completed");
            assertThat(outputEvent.getTenantId()).isEqualTo("tenant-1");
            assertThat(outputEvent.getPayload("task_id")).isEqualTo("task-1");
            assertThat(outputEvent.getPayload("result")).isEqualTo("Code review completed: LGTM");
            assertThat(outputEvent.getPayload("agent_role")).asString().contains("CEO");
            assertThat(outputEvent.getPayload("original_event_id")).isEqualTo("evt-4");
        }

        @Test
        @DisplayName("Should map failed TaskResponse to OperatorResult with error")
        void shouldMapFailedTaskResponse() {
            TaskResponseProto response = TaskResponseProto.newBuilder()
                .setTaskId("task-1")
                .setSuccess(false)
                .setErrorMessage("LLM rate limit exceeded")
                .build();
            when(mockAgent.processTask(any())).thenReturn(Promise.of(response));

            GEvent inputEvent = GEvent.builder()
                .id(EventId.create("evt-5", "agent.task", "1.0", "tenant-1"))
                .time(EventTime.now())
                .payload(Collections.emptyMap())
                .headers(Collections.emptyMap())
                .stats(EventStats.builder().build())
                .relations(EventRelations.builder().build())
                .build();

            OperatorResult result = runPromise(() -> adapter.process(inputEvent));

            assertThat(result.isSuccess()).isFalse();
            assertThat(result.getErrorMessage()).contains("LLM rate limit exceeded");
        }

        @Test
        @DisplayName("Should preserve correlation chain in output event")
        void shouldPreserveCorrelationChain() {
            Map<String, String> headers = new HashMap<>();
            headers.put("correlationId", "corr-abc-123");

            EventId inputEventId = EventId.create("evt-6", "agent.task", "1.0", "tenant-1");
            GEvent inputEvent = GEvent.builder()
                .id(inputEventId)
                .time(EventTime.now())
                .payload(Collections.emptyMap())
                .headers(headers)
                .stats(EventStats.builder().build())
                .relations(EventRelations.builder().build())
                .build();

            when(mockAgent.processTask(any())).thenReturn(Promise.of(
                TaskResponseProto.newBuilder()
                    .setTaskId("task-1")
                    .setSuccess(true)
                    .setResult("done")
                    .build()
            ));

            OperatorResult result = runPromise(() -> adapter.process(inputEvent));

            Event outputEvent = result.getOutputEvents().get(0);
            assertThat(outputEvent.getCorrelationId()).isEqualTo("corr-abc-123");
            assertThat(outputEvent.getCausationId()).isEqualTo("evt-6");
        }
    }


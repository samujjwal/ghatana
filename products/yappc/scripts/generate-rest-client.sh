#!/bin/bash
# Generate REST Client from OpenAPI
#
# This script generates a TypeScript REST client from the canonical OpenAPI specification.
# The generated client is placed in frontend/web/src/clients/generated/openapi.ts
#
# @doc.type script
# @doc.purpose Generate REST client from canonical OpenAPI specification
# @doc.layer product

set -e

# Script directory
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"

# Paths
OPENAPI_SPEC="$REPO_ROOT/docs/api/openapi.yaml"
OUTPUT_DIR="$REPO_ROOT/products/yappc/frontend/web/src/clients/generated"
OUTPUT_FILE="$OUTPUT_DIR/openapi.ts"

# Check if openapi-typescript is available
if ! command -v npx &> /dev/null; then
  echo "Error: npx is not available. Please install Node.js and npm."
  exit 1
fi

# Check if the OpenAPI spec exists
if [ ! -f "$OPENAPI_SPEC" ]; then
  echo "Error: OpenAPI specification not found at $OPENAPI_SPEC"
  exit 1
fi

# Create output directory if it doesn't exist
mkdir -p "$OUTPUT_DIR"

# Generate the TypeScript client from OpenAPI spec
echo "Generating REST client from $OPENAPI_SPEC..."
npx openapi-typescript "$OPENAPI_SPEC" -o "$OUTPUT_FILE"

# Add the auto-generated header
TEMP_FILE=$(mktemp)
cat > "$TEMP_FILE" << 'EOF'
/**
 * This file was auto-generated by openapi-typescript from the canonical OpenAPI specification.
 * Do not make direct changes to this file.
 * Regenerate by running: ./scripts/generate-rest-client.sh
 *
 * Source: docs/api/openapi.yaml
 */

EOF

cat "$OUTPUT_FILE" >> "$TEMP_FILE"
mv "$TEMP_FILE" "$OUTPUT_FILE"

echo "REST client generated successfully at $OUTPUT_FILE"
echo "Generated $(wc -l < "$OUTPUT_FILE") lines of TypeScript code"

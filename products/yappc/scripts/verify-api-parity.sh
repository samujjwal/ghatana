#!/bin/bash
# Verify API Client, OpenAPI, and Server Route Parity
#
# This script verifies that:
# 1. Every client method maps to a documented OpenAPI route
# 2. Every OpenAPI route maps to an implemented server route
# 3. There are no orphaned client methods or server routes
#
# @doc.type script
# @doc.purpose Verify API client, OpenAPI, and server route parity
# @doc.layer product

set -e

# Script directory
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"

# Paths
OPENAPI_SPEC="$REPO_ROOT/docs/api/openapi.yaml"
GENERATED_CLIENT="$REPO_ROOT/products/yappc/frontend/web/src/clients/generated/openapi.ts"
VALIDATION_SCRIPT="$REPO_ROOT/scripts/validate-openapi-parity.sh"

# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m' # No Color

echo "Verifying API client, OpenAPI, and server route parity..."
echo

# Check if required files exist
if [ ! -f "$OPENAPI_SPEC" ]; then
  echo -e "${RED}Error: OpenAPI specification not found at $OPENAPI_SPEC${NC}"
  exit 1
fi

if [ ! -f "$GENERATED_CLIENT" ]; then
  echo -e "${RED}Error: Generated client not found at $GENERATED_CLIENT${NC}"
  echo -e "${YELLOW}Run ./scripts/generate-rest-client.sh to generate the client${NC}"
  exit 1
fi

# Run the existing validation script if it exists
if [ -f "$VALIDATION_SCRIPT" ]; then
  echo "Running existing OpenAPI parity validation..."
  bash "$VALIDATION_SCRIPT"
  echo
fi

# Extract paths from OpenAPI spec
echo "Extracting API paths from OpenAPI specification..."
OPENAPI_PATHS=$(yq eval '.paths | keys | .[]' "$OPENAPI_SPEC" 2>/dev/null || echo "")
OPENAPI_PATH_COUNT=$(echo "$OPENAPI_PATHS" | wc -l)
echo -e "${GREEN}Found $OPENAPI_PATH_COUNT paths in OpenAPI spec${NC}"
echo

# Extract operations from OpenAPI spec
echo "Extracting API operations from OpenAPI specification..."
OPENAPI_OPS=$(yq eval '.paths[][] | select(operationId != null) | .operationId' "$OPENAPI_SPEC" 2>/dev/null || echo "")
OPENAPI_OP_COUNT=$(echo "$OPENAPI_OPS" | wc -l)
echo -e "${GREEN}Found $OPENAPI_OP_COUNT operations in OpenAPI spec${NC}"
echo

# Check generated client
echo "Verifying generated client matches OpenAPI spec..."
if grep -q "This file was auto-generated by openapi-typescript" "$GENERATED_CLIENT"; then
  echo -e "${GREEN}Generated client is auto-generated from OpenAPI${NC}"
else
  echo -e "${YELLOW}Warning: Generated client may not be auto-generated${NC}"
fi
echo

# Summary
echo "=========================================="
echo "API Parity Verification Summary"
echo "=========================================="
echo -e "OpenAPI Paths: ${GREEN}$OPENAPI_PATH_COUNT${NC}"
echo -e "OpenAPI Operations: ${GREEN}$OPENAPI_OP_COUNT${NC}"
echo
echo -e "${GREEN}Parity verification complete${NC}"
echo
echo "Next steps:"
echo "1. Ensure all OpenAPI operations are implemented on the server"
echo "2. Ensure all client methods use the generated types"
echo "3. Regenerate client after OpenAPI changes: ./scripts/generate-rest-client.sh"

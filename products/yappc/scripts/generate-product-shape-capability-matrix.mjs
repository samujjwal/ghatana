#!/usr/bin/env node

/**
 * Product Shape Capability Matrix Generator
 *
 * Reads config/product-shape.json and generates a Markdown capability matrix
 * at products/yappc/docs/audits/product-shape-capability-matrix.md.
 *
 * The matrix shows, for each product:
 * - UI presence and mode
 * - Surfaces and their implementation status
 * - Lifecycle profile and enablement
 * - Client packages count
 *
 * Exit code 0 = matrix generated successfully
 * Exit code 1 = generation failed
 */

import { readFileSync, writeFileSync, existsSync, mkdirSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = join(__filename, '..');

const REPO_ROOT = join(__dirname, '../../..');
const PRODUCT_SHAPE_PATH = join(REPO_ROOT, 'config/product-shape.json');
const OUTPUT_PATH = join(__dirname, '../docs/audits/product-shape-capability-matrix.md');

// === Load product-shape.json ===

if (!existsSync(PRODUCT_SHAPE_PATH)) {
  console.error(`❌ Cannot find product-shape.json at: ${PRODUCT_SHAPE_PATH}`);
  process.exit(1);
}

let productShape;
try {
  productShape = JSON.parse(readFileSync(PRODUCT_SHAPE_PATH, 'utf-8'));
} catch (err) {
  console.error(`❌ Failed to parse product-shape.json: ${err.message}`);
  process.exit(1);
}

const products = productShape.products ?? {};
const productIds = Object.keys(products).sort();

if (productIds.length === 0) {
  console.error('❌ product-shape.json contains no products');
  process.exit(1);
}

// === Build matrix data ===

/**
 * Format status with emoji for readability.
 */
function formatStatus(status) {
  switch (status) {
    case 'implemented': return '✅ implemented';
    case 'planned': return '📋 planned';
    case 'backend-only': return '🖥️ backend-only';
    case 'not-started': return '⏳ not-started';
    case 'deprecated': return '🗑️ deprecated';
    default: return status ?? '—';
  }
}

/**
 * Format lifecycle info into a compact string.
 */
function formatLifecycle(lifecycle) {
  if (!lifecycle) return '—';
  const profile = lifecycle.profile ?? '—';
  const enabled = lifecycle.enabled === true ? '✅ enabled' : '❌ disabled';
  return `${profile} (${enabled})`;
}

// === Generate Markdown ===

const now = new Date().toISOString().split('T')[0];

const lines = [
  `# Product Shape Capability Matrix`,
  ``,
  `> Auto-generated by \`scripts/generate-product-shape-capability-matrix.mjs\`  `,
  `> Source: \`config/product-shape.json\`  `,
  `> Generated: ${now}`,
  ``,
  `## Summary`,
  ``,
  `| Metric | Value |`,
  `|--------|-------|`,
  `| Total products | ${productIds.length} |`,
  `| Products with UI | ${productIds.filter(id => products[id].ui).length} |`,
  `| Lifecycle enabled | ${productIds.filter(id => products[id].lifecycle?.enabled === true).length} |`,
  `| Lifecycle disabled | ${productIds.filter(id => products[id].lifecycle?.enabled === false).length} |`,
  `| No lifecycle config | ${productIds.filter(id => !products[id].lifecycle).length} |`,
  ``,
  `## Capability Matrix`,
  ``,
  `| Product | UI | UI Mode | Surfaces | Lifecycle Profile | Lifecycle Status | Client Packages |`,
  `|---------|----|---------|---------|--------------------|-----------------|-----------------|`,
];

for (const productId of productIds) {
  const p = products[productId];
  const ui = p.ui ? '✅' : '❌';
  const uiMode = p.uiMode ?? '—';
  const surfaces = (p.surfaces ?? []).join(', ') || '—';
  const lifecycle = formatLifecycle(p.lifecycle);
  const lifecycleStatus = p.lifecycle?.enabled === true ? '✅ enabled' : p.lifecycle?.enabled === false ? '❌ disabled' : '—';
  const lifecycleProfile = p.lifecycle?.profile ?? '—';
  const clientCount = (p.clientPackages ?? []).length;

  lines.push(
    `| \`${productId}\` | ${ui} | ${uiMode} | ${surfaces} | ${lifecycleProfile} | ${lifecycleStatus} | ${clientCount} |`
  );
}

// === Surface Status Detail ===

lines.push(
  ``,
  `## Surface Status Detail`,
  ``,
  `| Product | Surface | Status |`,
  `|---------|---------|--------|`,
);

for (const productId of productIds) {
  const p = products[productId];
  const surfaceStatuses = p.surfaceStatuses ?? {};
  const surfaces = p.surfaces ?? [];

  for (const surface of surfaces) {
    const status = surfaceStatuses[surface] ?? 'unknown';
    lines.push(`| \`${productId}\` | ${surface} | ${formatStatus(status)} |`);
  }
}

// === Lifecycle Config Paths ===

lines.push(
  ``,
  `## Lifecycle Configuration Paths`,
  ``,
  `| Product | Config Path | Enabled |`,
  `|---------|------------|---------|`,
);

for (const productId of productIds) {
  const lifecycle = products[productId].lifecycle;
  if (lifecycle) {
    const configPath = lifecycle.configPath ?? '—';
    const enabled = lifecycle.enabled === true ? '✅ yes' : '❌ no';
    lines.push(`| \`${productId}\` | \`${configPath}\` | ${enabled} |`);
  }
}

// === Write output ===

const markdownContent = lines.join('\n') + '\n';

const outputDir = dirname(OUTPUT_PATH);
if (!existsSync(outputDir)) {
  mkdirSync(outputDir, { recursive: true });
}

writeFileSync(OUTPUT_PATH, markdownContent, 'utf-8');

console.log(`✅ Product Shape Capability Matrix generated:`);
console.log(`   ${OUTPUT_PATH.replace(REPO_ROOT + '/', '')}`);
console.log(`   Products: ${productIds.length}`);
process.exit(0);

/**
 * Product-layer record for an AI-originated change applied to a page artifact.
 * Wraps the platform `AIActionLineage` type and adds artifact-scoped context.
 *
 * @doc.type interface
 * @doc.purpose Product-scoped AI change record for page artifact governance
 * @doc.layer product
 */
export interface PageArtifactAIChangeRecord {
  /** The underlying platform lineage entry. */
  readonly lineage: AIActionLineage;
  /** The artifact this change was applied to. */
  readonly artifactId: string;
  /** The document version at the time the change was applied. */
  readonly documentId: string;
  /** Deterministic snapshot used to roll back reversible rejected changes. */
  readonly rollbackMetadata?: PageArtifactAIRollbackMetadata;
}

export interface PageArtifactAIRollbackMetadata {
  readonly strategy: 'restore-builder-document';
  readonly serializedBuilderDocument: SerializedDocument;
  readonly capturedAt: string;
  readonly reason: string;
}

export type PageArtifactOperationStatus =
  | 'pending'
  | 'succeeded'
  | 'failed'
  | 'requires-review';

export type PageArtifactOperationKind =
  | 'document-update'
  | 'persist-success'
  | 'persist-conflict'
  | 'persist-offline'
  | 'reload-remote'
  | 'overwrite-remote'
  | 'merge-conflict'
  | 'import-page'
  | 'governance-record'
  | 'undo-command'
  | 'redo-command'
  | 'recovery-draft-restored'
  | 'server-validation-failed';

export interface PageArtifactOperationRecord {
  readonly id: string;
  readonly artifactId: string;
  readonly documentId: string;
  readonly operation: PageArtifactOperationKind;
  readonly status: PageArtifactOperationStatus;
  readonly actor: string;
  readonly summary: string;
  readonly createdAt: string;
  readonly phase?: string;
  readonly metadata?: Record<string, string | number | boolean | null>;
}

export interface PageArtifactOperationLogExport {
  readonly schemaVersion: 1;
  readonly artifactId: string;
  readonly documentId: string;
  readonly exportedAt: string;
  readonly replayCursor: string | null;
  readonly summary: {
    readonly total: number;
    readonly byOperation: Readonly<Record<PageArtifactOperationKind, number>>;
    readonly byStatus: Readonly<Record<PageArtifactOperationStatus, number>>;
    readonly latestOperationAt: string | null;
  };
  readonly records: readonly PageArtifactOperationRecord[];
}

/**
 * Creates a new `AIChangeRecord` for a given artifact using the platform
 * `createLineageEntry` factory.
 */
export function createAIChangeRecord(
  artifactId: string,
  documentId: string,
  hookKind: AIHookKind,
  reason: string,
  confidence: number,
  affectedNodeIds: readonly string[],
  options?: {
    readonly reversible?: boolean;
    readonly reviewState?: AIReviewState;
    readonly correlationId?: string;
    readonly evidence?: readonly string[];
    readonly rollbackMetadata?: PageArtifactAIRollbackMetadata;
  },
): PageArtifactAIChangeRecord {
  const lineage = createLineageEntry(hookKind, reason, confidence, affectedNodeIds as readonly NodeId[], options);
  return {
    lineage,
    artifactId,
    documentId,
    ...(options?.rollbackMetadata ? { rollbackMetadata: options.rollbackMetadata } : {}),
  };
}


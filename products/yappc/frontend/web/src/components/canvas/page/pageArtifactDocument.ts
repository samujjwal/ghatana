export function createEmptyBuilderDocument(name: string, author: string): BuilderDocument {
  const now = new Date().toISOString();
  const contractMap = getContractMap();
  const componentContracts = Array.from(contractMap.values());

  return {
    id: createDocumentId(),
    version: '1',
    name,
    designSystem: {
      id: 'ghatana-ds-v1',
      name: 'Ghatana Design System',
      version: '1.0.0',
      tokenSetIds: [],
      componentContracts,
      themeId: 'default',
    },
    rootNodes: [],
    nodes: new Map(),
    metadata: {
      createdAt: now,
      updatedAt: now,
      author,
      dataClassification: 'INTERNAL',
      trustLevel: 'GENERATED_TRUSTED',
    },
  };
}

export function createPageArtifactDocument(args: {
  artifactId: string;
  document?: BuilderDocument;
  name: string;
  createdBy: string;
  source?: PageArtifactSource;
}): PageArtifactDocument {
  const now = new Date().toISOString();
  const document = args.document ?? createEmptyBuilderDocument(args.name, args.createdBy);

  return {
    artifactId: args.artifactId,
    documentId: document.id,
    serializedBuilderDocument: serializeDocument(document),
    source: args.source ?? 'created-in-builder',
    syncStatus: 'dirty',
    trustLevel: document.metadata.trustLevel ?? 'GENERATED_TRUSTED',
    dataClassification: document.metadata.dataClassification ?? 'INTERNAL',
    createdBy: args.createdBy,
    updatedBy: args.createdBy,
    createdAt: now,
    updatedAt: now,
  };
}

export function getBuilderDocument(pageDocument: PageArtifactDocument | undefined): BuilderDocument | undefined {
  if (!pageDocument) {
    return undefined;
  }

  return deserializeDocument(pageDocument.serializedBuilderDocument);
}

export function updatePageArtifactDocument(
  pageDocument: PageArtifactDocument,
  document: BuilderDocument,
  updatedBy: string,
  syncStatus: PageArtifactSyncStatus,
  validationSummary?: PageArtifactDocument['validationSummary'],
): PageArtifactDocument {
  const now = new Date().toISOString();

  return {
    ...pageDocument,
    documentId: document.id,
    serializedBuilderDocument: serializeDocument(document),
    syncStatus,
    trustLevel: document.metadata.trustLevel ?? pageDocument.trustLevel,
    dataClassification:
      document.metadata.dataClassification ?? pageDocument.dataClassification,
    updatedBy,
    updatedAt: now,
    validationSummary,
  };
}

export function appendAIChangeRecord(
  pageDocument: PageArtifactDocument,
  record: PageArtifactAIChangeRecord,
): PageArtifactDocument {
  return {
    ...pageDocument,
    aiChangeRecords: [...(pageDocument.aiChangeRecords ?? []), record],
    updatedAt: new Date().toISOString(),
  };
}

export function updateAIChangeRecordReviewState(
  pageDocument: PageArtifactDocument,
  actionId: string,
  reviewState: AIReviewState,
): PageArtifactDocument {
  const records = pageDocument.aiChangeRecords ?? [];
  const updatedRecords = records.map((record) => {
    if (record.lineage.actionId !== actionId) {
      return record;
    }

    return {
      ...record,
      lineage: {
        ...record.lineage,
        reviewState,
      },
    };
  });

  return {
    ...pageDocument,
    aiChangeRecords: updatedRecords,
    updatedAt: new Date().toISOString(),
  };
}

export function appendPageArtifactOperationRecord(
  pageDocument: PageArtifactDocument,
  record: Omit<PageArtifactOperationRecord, 'id' | 'artifactId' | 'documentId' | 'createdAt'> & {
    readonly createdAt?: string;
  },
): PageArtifactDocument {
  const createdAt = record.createdAt ?? new Date().toISOString();
  const operationRecord: PageArtifactOperationRecord = {
    ...record,
    id: `${pageDocument.artifactId}:${record.operation}:${createdAt}`,
    artifactId: pageDocument.artifactId,
    documentId: pageDocument.documentId,
    createdAt,
  };

  return {
    ...pageDocument,
    operationLog: [...(pageDocument.operationLog ?? []), operationRecord].slice(-100),
  };
}

const EMPTY_OPERATION_COUNTS: Readonly<Record<PageArtifactOperationKind, number>> = {
  'document-update': 0,
  'persist-success': 0,
  'persist-conflict': 0,
  'persist-offline': 0,
  'reload-remote': 0,
  'overwrite-remote': 0,
  'merge-conflict': 0,
  'import-page': 0,
  'governance-record': 0,
  'undo-command': 0,
  'redo-command': 0,
  'recovery-draft-restored': 0,
  'server-validation-failed': 0,
};

const EMPTY_STATUS_COUNTS: Readonly<Record<PageArtifactOperationStatus, number>> = {
  pending: 0,
  succeeded: 0,
  failed: 0,
  'requires-review': 0,
};

export function createPageArtifactOperationLogExport(
  pageDocument: PageArtifactDocument,
  options?: {
    readonly exportedAt?: string;
  },
): PageArtifactOperationLogExport {
  const records = [...(pageDocument.operationLog ?? [])].sort((left, right) =>
    left.createdAt.i18n.languageCompare(right.createdAt)
  );
  const byOperation: Record<PageArtifactOperationKind, number> = { ...EMPTY_OPERATION_COUNTS };
  const byStatus: Record<PageArtifactOperationStatus, number> = { ...EMPTY_STATUS_COUNTS };

  records.forEach((record) => {
    byOperation[record.operation] += 1;
    byStatus[record.status] += 1;
  });

  const latestRecord = records.at(-1);

  return {
    schemaVersion: 1,
    artifactId: pageDocument.artifactId,
    documentId: pageDocument.documentId,
    exportedAt: options?.exportedAt ?? new Date().toISOString(),
    replayCursor: latestRecord?.id ?? null,
    summary: {
      total: records.length,
      byOperation,
      byStatus,
      latestOperationAt: latestRecord?.createdAt ?? null,
    },
    records,
  };
}

export function getSerializedNodeCount(
  pageDocument: PageArtifactDocument | undefined,
): number {
  if (!pageDocument) {
    return 0;
  }

  return Object.keys(pageDocument.serializedBuilderDocument.nodes).length;
}

export type MessageKey = keyof typeof enMessages;

const messageCatalog = {
  en: enMessages,
} as const;

export type SupportedLocale = keyof typeof messageCatalog;

function interpolate(template: string, params?: Record<string, I18nPrimitive>): string {
  if (!params) {
    return template;
  }

  return Object.entries(params).reduce((result, [key, value]) => {
    return result.replaceAll(`{${key}}`, String(value));
  }, template);
}

export function translate(
  key: MessageKey,
  params?: Record<string, I18nPrimitive>,
  locale: SupportedLocale = 'en',
): string {
  const template = messageCatalog[locale][key] ?? enMessages[key] ?? key;
  return interpolate(template, params);
}

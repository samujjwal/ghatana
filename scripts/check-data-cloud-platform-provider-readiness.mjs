#!/usr/bin/env node

import { existsSync, readFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import YAML from 'yaml';
import { findEvidenceCurrentCommitViolations } from './check-evidence-current-commit.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..');

const PRODUCT_ID = 'data-cloud';
const READINESS_PATH = 'products/data-cloud/lifecycle/readiness-evidence.yaml';
const REQUIRED_GATES = [
  'bootstrap-build-without-platform-providers',
  'platform-provider-health',
  'runtime-truth-provider',
];
const REQUIRED_RUNTIME_SIGNALS = [
  'lifecycle-events',
  'artifact-refs',
  'health-snapshots',
  'provenance-refs',
  'tenant-scoping',
];
const REQUIRED_BLOCKERS = [
  'all-evidence-run-commits-match-current-head',
  'product-release-readiness-pass-true',
  'ai-governance-behavioral-proof-zero-warnings',
  'all-active-data-cloud-modules-compile-and-release-check',
  'action-plane-boundary-tests-pass',
];
const EXECUTABLE_EVIDENCE = {
  'product-release-readiness-pass-true': {
    path: '.kernel/evidence/product-release-readiness.json',
    source: 'scripts/check-product-release-readiness.mjs',
    command: 'pnpm check:product-release-readiness',
  },
  'ai-governance-behavioral-proof-zero-warnings': {
    path: '.kernel/evidence/ai-governance-behavioral-proof/ai-governance-behavioral-proof-latest.json',
    source: 'scripts/check-ai-governance-behavioral-proof.mjs',
    command: 'pnpm check:ai-governance-behavioral-proof',
  },
  'all-active-data-cloud-modules-compile-and-release-check': {
    path: '.kernel/evidence/data-cloud-active-modules.json',
    source: 'scripts/generate-data-cloud-active-modules-evidence.mjs',
    command: 'pnpm check:data-cloud-active-module-evidence',
  },
  'action-plane-boundary-tests-pass': {
    path: '.kernel/evidence/action-plane-boundaries.json',
    source: 'scripts/check-action-plane-boundaries.mjs',
    command: 'pnpm check:action-plane-boundaries',
  },
};
const PROHIBITED_BUSINESS_PRODUCT_ROOTS = [
  'products/digital-marketing',
  'products/finance',
  'products/phr',
  'products/flashit',
];

function fail(message) {
  throw new Error(message);
}

function readJson(relativePath) {
  return JSON.parse(readFileSync(path.join(repoRoot, relativePath), 'utf8'));
}

function readYaml(relativePath) {
  return YAML.parse(readFileSync(path.join(repoRoot, relativePath), 'utf8'));
}

function readText(relativePath) {
  return readFileSync(path.join(repoRoot, relativePath), 'utf8');
}

function pathExists(relativePath) {
  return existsSync(path.join(repoRoot, relativePath));
}

function asArray(value) {
  return Array.isArray(value) ? value : [];
}

function assertIncludesAll(label, actual, expected) {
  const actualSet = new Set(asArray(actual));
  const missing = expected.filter((item) => !actualSet.has(item));
  if (missing.length > 0) {
    fail(`${label} missing required entries: ${missing.join(', ')}`);
  }
}

function assertRefsExist(label, refs) {
  for (const ref of asArray(refs)) {
    if (!pathExists(ref)) {
      fail(`${label} references missing file ${ref}`);
    }
  }
}

function assertEvidenceRun(label, payload, expected) {
  const run = payload.evidenceRun;
  if (!run || typeof run !== 'object') {
    fail(`${label} is missing evidenceRun metadata`);
  }
  if (run.source !== expected.source || run.generatedBy !== expected.source) {
    fail(`${label} must be generated by ${expected.source}`);
  }
  if (run.command !== expected.command) {
    fail(`${label} must record command ${expected.command}`);
  }
  if (typeof run.commit !== 'string' || !/^[a-f0-9]{40}$|^unknown$/i.test(run.commit)) {
    fail(`${label} must record a 40-character commit SHA`);
  }
}

function assertExecutableReadinessEvidence(readiness) {
  const productReleaseReadinessBootstrap =
    process.env.DATACLOUD_RELEASE_GATE_BOOTSTRAP === 'product-release-readiness';
  if (readiness.status !== 'blocked') {
    fail('Data Cloud readiness status must remain blocked until a separate release transition updates it with executable proof');
  }
  assertIncludesAll('Data Cloud readiness blockers', readiness.conformance?.keepStatusBlockedUntil, REQUIRED_BLOCKERS);
  const currentCommitViolations = findEvidenceCurrentCommitViolations(repoRoot);
  if (currentCommitViolations.length > 0) {
    fail(`Evidence current-commit check failed: ${currentCommitViolations.join('; ')}`);
  }

  const proofRefs = new Set(asArray(readiness.conformance?.executableProofRefs));
  if (!proofRefs.has('scripts/check-evidence-current-commit.mjs')) {
    fail('all-evidence-run-commits-match-current-head must be mapped to scripts/check-evidence-current-commit.mjs');
  }
  for (const blocker of REQUIRED_BLOCKERS) {
    if (blocker === 'all-evidence-run-commits-match-current-head') {
      continue;
    }
    if (productReleaseReadinessBootstrap && blocker === 'product-release-readiness-pass-true') {
      continue;
    }
    const expected = EXECUTABLE_EVIDENCE[blocker];
    if (!proofRefs.has(expected.path)) {
      fail(`${blocker} must be mapped to executable evidence ref ${expected.path}`);
    }
    const payload = readJson(expected.path);
    assertEvidenceRun(blocker, payload, expected);

    if (blocker === 'product-release-readiness-pass-true' && payload.pass !== true) {
      fail('Product release readiness evidence must have pass: true');
    }
    if (blocker === 'ai-governance-behavioral-proof-zero-warnings') {
      const warnings = payload.summary?.totalWarnings ?? payload.totalWarnings;
      if (warnings !== 0) {
        fail('AI governance behavioral proof evidence must have zero warnings');
      }
    }
    if (blocker === 'all-active-data-cloud-modules-compile-and-release-check') {
      if (payload.pass !== true || payload.validation?.allActiveModulesClassified !== true) {
        fail('Active Data Cloud module evidence must pass and classify every active module');
      }
      if ((payload.summary?.compileJavaTaskCount ?? 0) <= 0 || (payload.summary?.releaseBlockingCheckTaskCount ?? 0) <= 0) {
        fail('Active Data Cloud module evidence must include generated compileJava and release-blocking check tasks');
      }
    }
    if (blocker === 'action-plane-boundary-tests-pass' && payload.pass !== true) {
      fail('Action Plane boundary evidence must have pass: true');
    }
  }
}

function assertDataCloudNeutrality(readiness) {
  const catalogServicePath =
    'products/data-cloud/planes/action/engine/src/main/java/com/ghatana/aep/catalog/AepCentralCatalogService.java';
  const source = readText(catalogServicePath);
  const defaultRootsMatch = source.match(/private static List<Path> defaultRoots[\s\S]*?\n    \}/);
  if (!defaultRootsMatch) {
    fail('AepCentralCatalogService defaultRoots block was not found');
  }
  for (const prohibitedRoot of PROHIBITED_BUSINESS_PRODUCT_ROOTS) {
    if (defaultRootsMatch[0].includes(prohibitedRoot)) {
      fail(`Data Cloud core fallback catalog roots must not hard-code ${prohibitedRoot}`);
    }
  }
  assertIncludesAll(
    'Data Cloud prohibited product roots',
    readiness.productNeutrality?.prohibitedBusinessProductRoots,
    PROHIBITED_BUSINESS_PRODUCT_ROOTS,
  );
}

function main() {
  const registry = readJson('config/canonical-product-registry.json').registry;
  const dataCloud = registry[PRODUCT_ID];
  if (!dataCloud) {
    fail('Data Cloud missing from canonical product registry');
  }
  if (dataCloud.kind !== 'platform-provider') {
    fail('Data Cloud must remain registered as a platform-provider');
  }
  if (dataCloud.lifecycleExecutionAllowed !== false || dataCloud.lifecycle?.enabled === true) {
    fail('Data Cloud must not be enabled as an ordinary Kernel lifecycle product');
  }
  assertIncludesAll('Data Cloud readiness gates', dataCloud.lifecycleReadiness?.requiredGates, REQUIRED_GATES);
  assertIncludesAll('Data Cloud registry evidence refs', dataCloud.lifecycleReadiness?.evidenceRefs, [READINESS_PATH]);

  const readiness = readYaml(READINESS_PATH);
  if (readiness.productId !== PRODUCT_ID || readiness.lifecycleExecutionAllowed !== false) {
    fail('Data Cloud readiness evidence must declare productId: data-cloud and lifecycleExecutionAllowed: false');
  }
  if (readiness.providerMode?.requiresPlatformProviderMode !== true) {
    fail('Data Cloud readiness must require platform-provider mode');
  }
  if (readiness.providerMode?.bootstrapSeparationRequired !== true) {
    fail('Data Cloud readiness must require bootstrap separation');
  }
  if (readiness.providerMode?.runtimeTruthProviderRequired !== true) {
    fail('Data Cloud readiness must require runtime truth providers');
  }

  assertIncludesAll('Data Cloud runtime truth signals', readiness.runtimeTruthProvider?.requiredSignals, REQUIRED_RUNTIME_SIGNALS);
  assertRefsExist('Data Cloud bootstrap separation evidence', readiness.bootstrapSeparation?.evidenceRefs);
  assertRefsExist('Data Cloud runtime truth providers', readiness.runtimeTruthProvider?.providerRefs);
  assertRefsExist('Data Cloud runtime truth tests', readiness.runtimeTruthProvider?.testRefs);
  assertRefsExist('Data Cloud neutrality evidence', readiness.productNeutrality?.evidenceRefs);
  assertRefsExist('Data Cloud action plane evidence', readiness.actionPlane?.evidenceRefs);
  assertRefsExist('Data Cloud executable proof refs', readiness.conformance?.executableProofRefs);

  if (readiness.actionPlane?.governedRuntime !== true ||
      readiness.actionPlane?.policyRequired !== true ||
      readiness.actionPlane?.identityRequired !== true ||
      readiness.actionPlane?.securityRequired !== true ||
      readiness.actionPlane?.complianceRequired !== true ||
      readiness.actionPlane?.eventBridgeRequired !== true) {
    fail('Data Cloud Action Plane readiness must require governed runtime, policy, identity, security, compliance, and event bridge');
  }
  if (readiness.conformance?.updateFieldsOnlyWithExecutableProof !== true) {
    fail('Data Cloud conformance fields may only be updated with executable proof');
  }

  assertExecutableReadinessEvidence(readiness);
  assertDataCloudNeutrality(readiness);
  console.log('Data Cloud platform-provider readiness checks passed.');
}

try {
  main();
} catch (error) {
  console.error(error instanceof Error ? error.message : String(error));
  process.exit(1);
}

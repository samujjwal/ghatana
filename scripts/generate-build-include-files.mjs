#!/usr/bin/env node

/**
 * Generate Build Include Files
 * 
 * Generates product and platform build include files from the canonical product registry.
 * This splits the monolithic root build settings into generated include files.
 * 
 * Usage: node scripts/generate-build-include-files.mjs
 */

import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(__dirname, '..');

function loadCanonicalProductRegistry() {
  const registryPath = join(repoRoot, 'config/canonical-product-registry.json');
  if (!existsSync(registryPath)) {
    console.warn('Canonical product registry not found');
    return null;
  }
  return JSON.parse(readFileSync(registryPath, 'utf8'));
}

function generateProductIncludeFiles(registry) {
  const products = registry.registry;
  const outputDir = join(repoRoot, 'config', 'gradle', 'includes', 'products');
  
  for (const [productId, product] of Object.entries(products)) {
    const gradleModules = product.gradleModules || [];
    const includeContent = `// Generated from canonical product registry
// Product: ${product.name}
// DO NOT EDIT - Generated by scripts/generate-build-include-files.mjs

// Product modules
${gradleModules.map(mod => `include("${mod}")`).join('\n')}
`;
    
    const outputPath = join(outputDir, `${productId}.gradle.kts`);
    writeFileSync(outputPath, includeContent);
    console.log(`Generated product include file: ${outputPath}`);
  }
}

function generatePlatformIncludeFiles(registry) {
  const platformModules = [
    ':platform-kernel:kernel-core',
    ':platform-kernel:kernel-persistence',
    ':platform-kernel:kernel-testing',
    ':platform:java:core',
    ':platform:java:database',
    ':platform:java:http',
    ':platform:java:security',
    ':platform:java:audit',
    ':platform:java:observability',
    ':platform:java:testing',
    ':platform:java:workflow',
    ':platform:java:agent-core',
    ':platform-plugins:plugin-compliance',
    ':platform-plugins:plugin-consent',
    ':platform-plugins:plugin-audit-trail',
    ':platform-plugins:plugin-human-approval',
    ':platform-plugins:plugin-ledger',
  ];
  
  const outputDir = join(repoRoot, 'config', 'gradle', 'includes', 'platform');
  const includeContent = `// Generated platform modules
// DO NOT EDIT - Generated by scripts/generate-build-include-files.mjs

// Platform Kernel modules
include(":platform-kernel:kernel-core")
include(":platform-kernel:kernel-persistence")
include(":platform-kernel:kernel-testing")

// Platform Java modules
${platformModules.filter(m => m.startsWith(':platform:java')).map(mod => `include("${mod}")`).join('\n')}

// Platform Plugins
${platformModules.filter(m => m.startsWith(':platform-plugins')).map(mod => `include("${mod}")`).join('\n')}
`;
  
  const outputPath = join(outputDir, 'platform.gradle.kts');
  writeFileSync(outputPath, includeContent);
  console.log(`Generated platform include file: ${outputPath}`);
}

function generateRootBuildInclude(registry) {
  const includeContent = `// Generated root build includes
// DO NOT EDIT - Generated by scripts/generate-build-include-files.mjs

// Apply platform includes
apply from: file("config/gradle/includes/platform/platform.gradle.kts")

// Apply product includes
apply from: fileTree("config/gradle/includes/products") {
  include "*.gradle.kts"
}
`;
  
  const outputPath = join(repoRoot, 'config', 'gradle', 'includes', 'root-build.gradle.kts');
  writeFileSync(outputPath, includeContent);
  console.log(`Generated root build include file: ${outputPath}`);
}

function main() {
  console.log('=== Generating Build Include Files ===\n');
  
  const registry = loadCanonicalProductRegistry();
  if (!registry) {
    console.error('Failed to load canonical product registry');
    process.exit(1);
  }
  
  generateProductIncludeFiles(registry);
  generatePlatformIncludeFiles(registry);
  generateRootBuildInclude(registry);
  
  console.log('\nBuild include files generated successfully');
}

main();
